class Node {
  constructor(row, col) {
    this.row = row;
    this.col = col;
    this.mouseDown = false;
    this.isStart = this.isStart();
    this.isFinish = this.isFinish();
    this.isWall = this.isWall();
    this.previousNode = null;
    this.distance = Infinity;
  }

  isWall() {
    const r = this.row;
    const c = this.col;
    if (WALL_NODES[r][c]) {
      this.addClass("wall");
      return true;
    };
    return false;
  }

  isStart() {
    if (this.row === START_NODE_ROW && this.col === START_NODE_COL) {
      this.addClass("node-start");
      return true;
    };
    return false;
  }

  isFinish() {
    if (this.row === FINISH_NODE_ROW && this.col === FINISH_NODE_COL) {
      this.addClass("node-finish");
      return true
    };
    return false;
  }

  addClass(className) {
    let nodeEle = document.getElementById(`${this.row}-${this.col}`)
    nodeEle.className += ` ${className}`;
  }
}

export default Node;